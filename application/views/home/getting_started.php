<h4>How do I start fencing?</h4>
<div class="block">
<p>
The obvious answer is: learn how to fence. There are several clubs in 
South Australia, all of which offer beginners classes. During a 
beginners class you'll be taught how to hold a foil, how to stand in on
guard, how to put on the protective clothing, the basic rule, footwork,
attacks, parries etc. After you complete a course you should know everything
you need to know to stand on the strip and fence a training bout, or compete
in a novice competition.
</p>

<h4>Where are beginner's classes offered?</h4>
<p>
Beginner's classes are offered at any of the fencing clubs in South Australia. 
See contacts - <? echo anchor('clubs','clubs'); ?> for contact details.
</p>

<h4>What equipment will I need?</h4>
<p>
All the protective equipment you need at first will be provided by your fencing club, however over time it is expected that you will begin to purchase your own equipment.
</p>

<h4>How much do beginners courses cost?</h4>
<p>
Registration with Fencing SA costs twenty dollar. The cost of beginner's
courses varies from club to club. However one hundred dollars is a fairly
common price for a course lasting several weeks. There may be additional fees
such as club membership and equipment rental.
</p>

<h4>Where do I go for more information?</h4>
<p>
For more information about specific clubs contact the clubs designated contact. 
For more general information contact Fencing SA: phone 0423 822 817 or email 
<a href="mailto:secretary@fencingsa.org.au">secretary@fencingsa.org.au</a>.
</p>
